class ViewPlaceholders {
    
    /**
     * The Page instance associated with current placeholders object
     * 
     */
    protected $page;
    
    /**
     * Directory containing view scripts
     * 
     */
    protected $scripts;
    
    /**
     * Template extension
     * 
     */
    protected $ext;

    /**
     * Container for data
     * 
     */
    protected $data = array();
    
    /**
     * Constructor method
     * 
     * @param Page $page
     * @param string $scripts
     * @param string $ext
     * @throws Exception if scripts directory is missing or unreadable
     * @throws Exception if invalid format is used for template extension
     */
    public function __construct(Page $page, $scripts, $ext) {
        if (!is_dir($scripts)) {
            throw new Exception("Missing or unreadable scripts directory");
        }
        if (!is_string($ext) || basename($ext) != $ext || strpos($ext, ".") !== 0) {
            throw new Exception("Invalid format for template extension");
        }
        $this->page = $page;
        $this->scripts = $scripts;
        $this->ext = $ext;
    }
    
    /**
     * Return content from a named view placeholder *or* markup generated by
     * rendering the page using a view script matching the placeholder name
     * 
     * @param string $key
     * @return mixed
     */
    public function __get($key) {
        $return = isset($this->data[$key]) ? $this->data[$key] : null;
        if (is_null($return) && basename($key) == $key) {
            if (is_file($this->scripts . $this->page->template . "/" . $key . $this->ext)) {
                $page_layout = $this->page->layout();
                $page_view = $this->page->view();
                $this->page->_pwmvc_context = "placeholder";
                $return = $this->page->layout('')->view($key)->render();
                unset($this->page->_pwmvc_context);
                if ($page_layout !== '') $this->page->layout($page_layout);
                if ($page_view !== $key) $this->page->view($page_view);
            }
        }
        return $return;
    }

    /**
     * Store values to the protected $data array
     * 
     * @param string $key
     * @param mixed $value
     * @return ViewPlaceholders
     */
    public function __set($key, $value) {
        $this->data[$key] = $value;
        return $this;
    }
    
}
